package Final_activity;

public class Calculator {

	public int getSum(int a, int b) {
		
		return a + b;
	}
	
	public int getDifference(int a, int b){
		return a - b;
	}
	
	public double getProduct(double a, double b){
		return a * b;
	}
	
	public double getQuotient(double a, double b){
		return a / b;
	}
	
	
}
